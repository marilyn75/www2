                    <h3>제20조 (이용요금의 환불)</h3>
                    <p>① 유료서비스 결제 후 회원이 결제기간 내에 실제 서비스를 이용하지 않았다는 점은 원칙적으로 환불사유가 되지 않습니다.
                        ② 회원이 착오, 사기, 강박 등의 사유로 사회경제적으로 현저하게 과도한 결제를 한 경우 환불대상이 될 수 있습니다. 이 경우 회원의 착오나 사기 등의 사유 및 사회경제적으로 현저하게 과도한 결제를 하였음은 회원 본인이 합리적인 방법으로 증명하여야 합니다.
                        ③ 회원이 제2항의 규정에 따라 환불을 받는 경우 환불 받을 금액 및 계좌번호, 환불 일시 등에 관하여는 회사와 회원간 협의에 의합니다.
                        ④ 회사가 제12조 제1항 제2호, 제3호 내지 제6호에 따른 사유가 발생하여 서비스의 일부 또는 전부를 제한하거나 중지하는 경우 해당 사유가 발생한 시점의 유료결제기간 내에 있는 회원 전원 또는 해당 사유와 직접적으로 관련이 있는 일부 회원에 대해서 환불조치를 할 수 있습니다. 이 경우 회사는 환불 받을 금액 및 환불 일시 및 구체적 방법에 관하여 해당 사유가 발생한 즉시 회원이 즉시 인지할 수 있는 합리적인 방법을 통하여 공지하여야 합니다.
                        ⑤ 회원이 제14조의 규정을 위반하여 회사가 회원의 서비스 이용을 제한하거나 일방적으로 본 계약을 해지하는 경우, 회사는 회원에게 일체의 환불을 하지 않습니다.</p>
