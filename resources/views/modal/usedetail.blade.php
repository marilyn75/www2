                        ⑥ 회사는 결제의 이행을 위해 필요한 회원의 개인정보 및 본인인증을 추가로 요구할 수 있으며, 회원은 회사가 요구하는 개인정보를 정확하게 제공하고 적절한 본인인증 방식에 따라야 합니다. 회사는 회원이 허위로 또는 부정확하게 제공한 개인정보로 인하여 회원에게 발생하는 손해에 대하여 회사의 고의•과실이 없는 한 회원에게 별도의 손해배상책임을 부담하지 않습니다.
                        ⑦ 요금을 납입할 자가 납입 기일 내에 납입하지 않은 경우, 회사는 별도의 사전 통지 없이 해당 유료 서비스의 제공을 중단할 수 있습니다.
                        ⑧ 유료서비스를 구매한 신용카드 결제가 연체되어 신용카드 회사로부터 카드가 무효 또는 판매부인의 통지가 있는 경우, 회사는 즉시 해당 회원의 유료 서비스 이용을 중단시키고, 회원에 대해 대금 결제방법의 변경 여부에 대한 확인 연락을 합니다. 단, 신용카드 회원이 회사에 제출한 연락처로 연락이 되지 않을 경우 또는 지정일까지 확인이 되지 않는 경우 회사는 당해 회원과의 이용계약을 해지하고 당해 결제 건에 대해서 서비스를 제공하지 않습니다.
                        ⑨ 회사는 유료서비스 이용 금액에 대하여 회원의 요청이 있는 경우에만 현금영수증 또는 세금계산서를 발행합니다.</p>
                </div>
        
                <div class="agr_pg">
                    <h3>제19조 (이용요금에 대한 이의신청)</h3>
                    <p>① 회원은 이용요금에 대하여 이의를 제기할 수 있습니다. 단, 이용요금에 관한 이의는 그 사유의 발생을 안 날로부터 1개월, 그 사유가 발생한 날로부터 3개월 이내에 제기하여야 합니다.
                        ② 회사는 이용요금 청구 등에 오류가 있는 경우 아래의 조치를 합니다.
                        회원이 과다 납입한 요금에 대해서는 제19조의 규정에 따릅니다. 과소 청구한 요금에 대해서 회사는 미수금을 재청구하고, 미수금이 완납될 때까지 회원의 서비스 이용을 제한 또는 중단할 수 있습니다.</p>
